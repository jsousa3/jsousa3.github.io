uma_lista = [10, 'hello', False]
print(uma_lista)
# acrescentar um float no final da lista
uma_lista.append(2.0)
print(uma_lista)
# inserir uma string no terceiro elemento
uma_lista.insert(2, 'world')
print(uma_lista)
# remover o primeiro elemento
elemento_removido = uma_lista.pop(0)
print(uma_lista)
print(elemento_removido)
