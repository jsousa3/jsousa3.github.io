lista_de_preços = [97, 70, 15, 38, 92, 30, 86, 68, 6, 80]
print(lista_de_preços)
# acrescentar um preço no final da lista
lista_de_preços.append(100)
print(lista_de_preços)
# inserir um preço no terceiro elemento
lista_de_preços.insert(2, 200)
print(lista_de_preços)
# remover o primeiro preço
preço_removido = lista_de_preços.pop(0)
print(lista_de_preços)
print(preço_removido)
