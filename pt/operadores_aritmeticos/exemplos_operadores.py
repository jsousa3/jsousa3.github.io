# adição
print(7 + 2)
# subtração
print(7 - 2)
# multiplicação
print(7 * 2)
# divisão (o resultado é sempre um float)
print(7 / 2)
# exponenciação
print(7 ** 2)
# divisão inteira (o resultado é sempre um inteiro)
print(7 // 2)
# resto da divisão inteira
print(7 % 2)
# esta linha dá erro
