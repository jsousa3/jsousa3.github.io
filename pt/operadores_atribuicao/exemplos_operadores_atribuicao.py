x = 0
print(x)

x += 1 # é equivalente a x = x + 1
print(x)

x -= 2 # é equivalente a x = x - 2
print(x)

x *= 3 # é equivalente a x = x * 3
print(x)

x /= 4 # é equivalente a x = x / 4
print(x)

x **= 5 # é equivalente a x = x ** 5
print(x)

x %= 6 # é equivalente a x = x % 6
print(x)

x //= 7 # é equivalente a x = x // 7
print(x)
