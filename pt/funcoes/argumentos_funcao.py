
# esta função tem um argumento
def dobro(x):

    return 2 * x

# esta função tem três argumentos
def produto(x, y):

    return x * y

# esta função não tem argumentos
def hello():

    print('Hello World!')
    
print(dobro(10))
print(produto(10,20))
print(hello())
print(dobro(1, 2))
print(produto(3))
print(hello(4))
